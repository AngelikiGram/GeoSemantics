@app.route('/api/morph/all_places_umap')
def all_places_umap():
    """
    Slim endpoint: {name, lat, lon, ux, uy, dominant_dim, place_type, is_seed}
    for ALL Austrian places (generated by precompute_all_places.py).

    Optional query params:
      bbox=lat_min,lon_min,lat_max,lon_max  — server-side spatial filter
    """
    places = _all_places.get('places', [])

    bbox_str = request.args.get('bbox')
    if bbox_str:
        try:
            lat_min, lon_min, lat_max, lon_max = map(float, bbox_str.split(','))
            places = [
                p for p in places
                if lat_min <= p['lat'] <= lat_max and lon_min <= p['lon'] <= lon_max
            ]
        except Exception:
            pass  # ignore malformed bbox

    # Slim fields only — no semantic_dims (saves bandwidth for large sets)
    slim = [{
        'name':         p['name'],
        'lat':          p['lat'],
        'lon':          p['lon'],
        'ux':           p.get('ux', 0.5),
        'uy':           p.get('uy', 0.5),
        'dominant_dim': p.get('dominant_dim', 'Community'),
        'place_type':   p.get('place_type', 'village'),
        'is_seed':      p.get('is_seed', False),
    } for p in places]

    return jsonify({
        'places':   slim,
        'n_places': len(slim),
        'total':    _all_places.get('n_places', 0),
        'method':   _all_places.get('method', 'unknown'),
    })

SEMANTIC_GRID_PATH = os.path.join('_poi_cache', 'semantic_grid.json')

@app.route('/api/morph/semantic_grid')
def semantic_grid():
    """
    Dense Austria-wide character grid for the 'Character Layer' map overlay
    (generated by precompute_semantic_grid.py). Read fresh from disk on every
    request — the precompute script checkpoints every 200 cells, so toggling
    the layer on always reflects whatever progress has been made so far,
    with no server restart needed while that job is still running.

    Optional query params:
      bbox=lat_min,lon_min,lat_max,lon_max  — server-side spatial filter
    """
    if not os.path.exists(SEMANTIC_GRID_PATH):
        return jsonify({'cells': [], 'n_cells': 0, 'has_data': False,
                         'note': 'Run: python precompute_semantic_grid.py'})
    try:
        with open(SEMANTIC_GRID_PATH, 'r', encoding='utf-8') as fh:
            data = json.load(fh)
    except Exception as e:
        return jsonify({'cells': [], 'n_cells': 0, 'has_data': False, 'error': str(e)})

    cells = data.get('cells', [])
    bbox_str = request.args.get('bbox')
    if bbox_str:
        try:
            lat_min, lon_min, lat_max, lon_max = map(float, bbox_str.split(','))
            cells = [c for c in cells
                     if lat_min <= c['lat'] <= lat_max and lon_min <= c['lon'] <= lon_max]
        except Exception:
            pass  # ignore malformed bbox

    return jsonify({
        'cells':    cells,
        'n_cells':  len(cells),
        'grid':     data.get('grid'),
        'radius':   data.get('radius'),
        # Matches precompute_semantic_grid.py's LAT_RANGE/LON_RANGE — sent
        # along so the frontend can size each cell to the grid spacing
        # without duplicating those constants independently.
        'lat_range': [46.30, 49.05],
        'lon_range': [9.40, 17.20],
        'has_data': len(cells) > 0,
    })

