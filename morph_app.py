def _dim_summary_phrase(dims):
    """'predominantly Nature (81%) with a secondary Transport presence (12%)'"""
    ranked = sorted(dims.items(), key=lambda kv: kv[1], reverse=True)
    ranked = [(k, v) for k, v in ranked if v > 0.01]
    if not ranked:
        return 'no clear dominant character'
    top_k, top_v = ranked[0]
    phrase = f'predominantly <b>{top_k}</b> ({round(top_v*100)}%)'
    if len(ranked) > 1 and ranked[1][1] >= 0.10:
        k2, v2 = ranked[1]
        phrase += f' with a secondary <b>{k2}</b> presence ({round(v2*100)}%)'
    return phrase


def _influential_features_phrase(top_pois, max_items=4):
    """'Großglockner, Pasterze Glacier, and 8 hiking paths' — prefers genuinely
    named landmarks (where OSM gave a real name, not just a category fallback)
    over generic category mentions, and groups remaining same-category POIs
    with a count instead of repeating the category name."""
    if not top_pois:
        return None, []
    named, named_lc, cat_counts, cat_order = [], [], {}, []
    for p in top_pois[:20]:               # only the most saliency-influential POIs
        name, value, cat = p.get('name', ''), p.get('value', ''), p.get('category', '')
        name = name.strip()
        # A "real" name must differ from the generic category fallback AND
        # look like an actual name — OSM occasionally has junk values like
        # "-2" or a bare house number in the name field.
        is_real_name = (bool(name) and name.lower() != str(value).strip().lower()
                         and len(name) >= 2 and any(ch.isalpha() for ch in name))
        if is_real_name:
            nlc = name.strip().lower()
            # Skip near-duplicates: same name in another case, or one name
            # contained inside another (e.g. "Hallstatt" vs "Enchanting Hallstatt")
            if any(nlc == e or nlc in e or e in nlc for e in named_lc):
                continue
            named.append(name)
            named_lc.append(nlc)
        else:
            label = value or cat or 'feature'
            # Same plausibility check — category fallback values can also be
            # OSM junk (e.g. a bare "-2" from a level/entrance tag).
            if not (len(label.strip()) >= 2 and any(ch.isalpha() for ch in label)):
                label = cat if (cat and any(ch.isalpha() for ch in cat)) else 'feature'
            if label not in cat_counts:
                cat_order.append(label)
            cat_counts[label] = cat_counts.get(label, 0) + 1

    parts = list(named)
    for label in cat_order:
        n = cat_counts[label]
        parts.append(f'{n} {label}{"s" if n > 1 else ""}' if n > 1 else label)
        if len(parts) >= max_items:
            break
    parts = parts[:max_items]

    if not parts:
        return None, []
    if len(parts) == 1:
        joined = parts[0]
    elif len(parts) == 2:
        joined = f'{parts[0]} and {parts[1]}'
    else:
        joined = ', '.join(parts[:-1]) + f', and {parts[-1]}'
    return joined, named


def _nl_lens(loc):
    dims  = loc.get('semantic_dims', {})
    td    = max(dims, key=dims.get) if dims else '–'
    sa    = loc.get('scale_attention', [1/3]*3)
    si    = int(np.argmax(sa))
    sp    = round(sa[si] * 100)
    descs = [
        "street-level character dominates — defined by what's immediately around you",
        'neighbourhood context shapes this place — the surrounding quarter matters most',
        'city-level structure defines identity — district type matters more than individual streets',
    ]
    arch  = loc.get('archetype_similarities', {})
    ba    = max(arch, key=arch.get) if arch else '–'
    bap   = round((arch.get(ba, 0) + 1) / 2 * 100) if arch else 0
    n     = loc.get('n_pois', 0)
    c     = loc.get('confidence', 0)
    label = loc.get('label', '–')

    if label == '–' or not label or label.strip() == '':
        closest_pin = loc.get('closest_pin_name', '–')
        if ' · ' in closest_pin:
            closest_pin = closest_pin.split(' · ')[0]
        if closest_pin and closest_pin != '–':
            archetype_phrase = f'an area near {closest_pin}'
        else:
            archetype_phrase = 'an unlabelled area'
    else:
        archetype_phrase = _LABEL_PHRASE.get(label, f'an area labelled "{label}"')
    dim_phrase = _dim_summary_phrase(dims)
    why_phrase, named = _influential_features_phrase(loc.get('top_pois', []))

    why_sentence = ''
    if why_phrase:
        verb = 'anchored by' if named else 'shaped primarily by'
        why_sentence = f'This reading is {verb} {why_phrase}. '

    confidence_caveat = ''
    if c < 0.4:
        confidence_caveat = ' — sparse OSM data here, so treat this reading cautiously'

    return (f'<b>{loc["name"]}</b> reads as <b>{archetype_phrase}</b> — {dim_phrase}. '
            f'{why_sentence}'
            f'Identity is set at the <b>{SCALE_LABELS[si]}</b> scale ({sp}%): {descs[si]}. '
            f'Closest comparable archetype: <b>{ba}</b> ({bap}% similarity). '
            f'Based on {n} mapped features here · {c:.0%} confidence{confidence_caveat}.')

def _nl_diff(la, lb, sim_pct, deltas):
    da, db = la.get('semantic_dims', {}), lb.get('semantic_dims', {})
    ta = max(da, key=da.get) if da else '–'
    tb = max(db, key=db.get) if db else '–'
    desc = ('very similar' if sim_pct > 70 else 'moderately similar'
            if sim_pct > 50 else 'distinct' if sim_pct > 35 else 'contrasting')
    sa, sb = la.get('scale_attention', []), lb.get('scale_attention', [])
    sn = ['Micro', 'Meso', 'Macro']
    doma = sn[int(np.argmax(sa))] if sa else '–'
    domb = sn[int(np.argmax(sb))] if sb else '–'
    top = sorted(deltas.items(), key=lambda x: abs(x[1]), reverse=True)
    note = ''
    if top:
        k, v = top[0]
        note = (f'The sharpest contrast is <b>{k}</b>: '
                f'{"A" if v < 0 else "B"} scores {abs(round(v*100))}pp higher. ')
    return (f'<b>{la["name"]}</b> ↔ <b>{lb["name"]}</b> are <b>{desc}</b> ({sim_pct}%). '
            f'{note}'
            f'A is <b>{ta.lower()}</b>-dominant and <b>{doma}</b>-scale; '
            f'B is <b>{tb.lower()}</b>-dominant and <b>{domb}</b>-scale.')

def _nl_morph(la, lb, dims_t, sa_t, arch_t, t):
    td  = max(dims_t, key=dims_t.get) if dims_t else '–'
    tp  = round(dims_t.get(td, 0) * 100)
    si  = int(np.argmax(sa_t))
    sp  = round(sa_t[si] * 100)
    ba  = max(arch_t, key=arch_t.get) if arch_t else '–'
    pct = round(t * 100)
    da, db = la.get('semantic_dims', {}), lb.get('semantic_dims', {})
    ta = max(da, key=da.get) if da else '–'
    tb = max(db, key=db.get) if db else '–'
    if pct < 20:
        trans = f'strongly resembles <b>{la["name"]}</b> — mostly {ta.lower()}'
    elif pct > 80:
        trans = f'strongly resembles <b>{lb["name"]}</b> — mostly {tb.lower()}'
    else:
        trans = (f'blending <b>{ta.lower()}</b> → <b>{tb.lower()}</b> '
                 f'(currently {td.lower()} at {tp}%)')
    return (f'At <b>{pct}%</b> from <b>{la["name"]}</b> to <b>{lb["name"]}</b>, '
            f'identity {trans}. '
            f'Scale anchor: <b>{SCALE_LABELS[si]}</b> ({sp}%). '
            f'Closest archetype: <b>{ba}</b>.')

def _build_transition_explanation(la, lb, dims_a, dims_b):
    """
    Build a detailed transition explanation answering:
    - What changes first / most?
    - Which dimension changes most?
    - Which scale drives the change?
    - Which node types appear / disappear?
    """
    da = la.get('semantic_dims', dims_a)
    db = lb.get('semantic_dims', dims_b)
    all_dims = set(da) | set(db)

    # Dimension deltas (B - A)
    deltas = {k: round(db.get(k, 0) - da.get(k, 0), 3) for k in all_dims}
    sorted_deltas = sorted(deltas.items(), key=lambda x: abs(x[1]), reverse=True)

    # What changes most?
    top_k, top_v = sorted_deltas[0] if sorted_deltas else ('–', 0)
    direction    = 'increases' if top_v > 0 else 'decreases'

    # Which dims appear / disappear above threshold
    threshold = 0.08
    appears   = [k for k, v in deltas.items() if v  >  threshold]
    fades     = [k for k, v in deltas.items() if v  < -threshold]
    stable    = [k for k in all_dims
                 if da.get(k, 0) >= threshold and db.get(k, 0) >= threshold
                 and abs(deltas.get(k, 0)) < threshold]

    # Scale attention shift
    sa_a  = la.get('scale_attention', [1/3]*3)
    sa_b  = lb.get('scale_attention', [1/3]*3)
    dom_a = int(np.argmax(sa_a))
    dom_b = int(np.argmax(sa_b))
    scale_shift = ''
    if dom_a != dom_b:
        scale_shift = (f'Scale anchor shifts from <b>{SCALE_SHORT[dom_a]}</b> '
                       f'to <b>{SCALE_SHORT[dom_b]}</b> along the path. ')
    else:
        scale_shift = f'Scale remains <b>{SCALE_SHORT[dom_a]}</b>-dominant throughout. '

    # Build NL
    nl_parts = []
    if top_k != '–':
        nl_parts.append(
            f'<b>{top_k}</b> {direction} most sharply '
            f'({abs(round(top_v*100))} pp).')
    if appears:
        nl_parts.append(f'Emerging at B: <b>{", ".join(appears)}</b>.')
    if fades:
        nl_parts.append(f'Fading toward B: <b>{", ".join(fades)}</b>.')
    if stable:
        nl_parts.append(f'Stable throughout: {", ".join(stable)}.')
    nl_parts.append(scale_shift)

    return {
        'top_changing_dim':   top_k,
        'top_delta_pp':       round(top_v * 100, 1),
        'appears_at_b':       appears,
        'fades_toward_b':     fades,
        'stable_dims':        stable,
        'scale_shift':        scale_shift,
        'nl':                 ' '.join(nl_parts),
        'dim_deltas':         sorted_deltas[:7],
    }

