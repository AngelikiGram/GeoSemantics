def morph_temporal_pois():
    body = request.get_json(force=True)
    lat  = float(body['lat'])
    lon  = float(body['lon'])
    year = body.get('year')
    if str(year).lower() in ['now', 'none', '']:
        year = None
    else:
        year = int(year)

    try:
        import temporal as tmp
        import geo_snapshot as gs
        import inference as inf
        import torch
        import numpy as np
        
        records = tmp.fetch_year_records(lat, lon, year, radius=700)
        if not records:
            return jsonify({'pois': []})
        df = gs.records_to_df(records)
        res = gs.build_resources(df)
        g, sel_df = gs.local_graph(lat, lon, 600, res)
        if g is None:
            return jsonify({'pois': []})

        with torch.no_grad():
            sal = inf._saliency_model(g.x, g.edge_index).numpy()
        sal = sal.squeeze() if sal.ndim > 1 else sal
        s_min = sal.min(); s_range = (sal.max() - s_min) + 1e-8
        sal_n = (sal - s_min) / s_range
        
        pois = []
        for i, (_, row) in enumerate(sel_df.iterrows()):
            cat, val = inf.get_poi_label(row)
            pois.append({'name': str(row.get('name', '') or val), 'category': cat,
                         'value': val, 'saliency': round(float(sal_n[i]), 4),
                         'lat': float(row['lat']), 'lon': float(row['lon'])})
        pois.sort(key=lambda p: p['saliency'], reverse=True)
        return jsonify({'pois': pois})
    except Exception as e:
        import traceback; traceback.print_exc()
        return jsonify({'error': str(e)}), 500

