def _build_live_loc(lat, lon, radius=600, model='auto'):
    """Run GNN query and build a full location dict.

    model: 'v2' | 'v3' | 'auto'
      'auto' = prefer V3 character + V2 embedding (original behaviour)
      'v2'   = force V2 character + V2 embedding
      'v3'   = force V3 character + V3 embedding
    Returns None on failure.
    """
    if _inf is None:
        return None
    sal, sel_df, _ = _inf.get_saliency(lat, lon, radius)
    if sal is None or len(sal) == 0:
        return None

    # Character selection
    want_v3_char = model in ('v3', 'auto')
    want_v2_char = model in ('v2', 'auto', 'v1')
    char = None
    if want_v3_char and hasattr(_inf, 'get_location_character_v3') and _inf._v3_node_types is not None:
        char = _inf.get_location_character_v3(lat, lon, radius)
    if char is None and want_v2_char:
        char = _inf.get_location_character(lat, lon, radius)
    if char is None:
        return None

    # Confidence score
    conf_result = None
    if hasattr(_inf, 'get_confidence_score'):
        conf_result = _inf.get_confidence_score(lat, lon, radius)
    confidence = conf_result['score'] if conf_result else (
        round(min(1.0, len(sal)/200)*0.45 + float(np.clip(sal.mean(), 0, None))*0.55, 3))

    # Multi-scale character
    multiscale = None
    if hasattr(_inf, 'get_multiscale_character'):
        try:
            multiscale = _inf.get_multiscale_character(lat, lon)
        except Exception:
            multiscale = None

    s_min   = sal.min(); s_range = (sal.max() - s_min) + 1e-8
    sal_n   = (sal - s_min) / s_range
    pois    = []
    for i, (_, row) in enumerate(sel_df.iterrows()):
        cat, val = _inf.get_poi_label(row)
        pois.append({'name': str(row.get('name', '') or val), 'category': cat,
                     'value': val, 'saliency': round(float(sal_n[i]), 4),
                     'lat': float(row['lat']), 'lon': float(row['lon'])})
    pois.sort(key=lambda p: p['saliency'], reverse=True)

    # Embedding selection
    emb, scale_attn, char_src = None, [1/3]*3, char.get('source', 'v2')
    prefer_v3_emb = (model == 'v3')
    
    if model == 'v1':
        emb_v1, _ = _inf.get_embedding(lat, lon, radius)
        if emb_v1 is not None:
            emb = emb_v1.tolist()
            char_src = 'v1'
    elif not prefer_v3_emb and _inf.v2_available:
        emb_v2, attn = _inf.get_embedding_v2(lat, lon)
        if emb_v2 is not None:
            emb        = emb_v2.tolist()
            scale_attn = attn.tolist() if attn is not None else scale_attn
            char_src   = 'v2'
    if emb is None and _inf.v3_available:
        emb_v3, attn = _inf.get_embedding_v3(lat, lon)
        if emb_v3 is not None:
            emb        = emb_v3.tolist()
            scale_attn = attn.tolist() if attn is not None else scale_attn
    # v3 explicit: try v3 first, then v2 fallback
    if prefer_v3_emb and emb is None and _inf.v2_available:
        emb_v2, attn = _inf.get_embedding_v2(lat, lon)
        if emb_v2 is not None:
            emb        = emb_v2.tolist()
            scale_attn = attn.tolist() if attn is not None else scale_attn

    nearest_id = _nearest_emb(emb) if emb else -1
    if nearest_id < 0:
        nearest_id, _ = _nearest_geo(lat, lon)
    nearest = _loc_by_id(nearest_id)

    return {
        'name':                   f'{lat:.4f}, {lon:.4f}',
        'lat': lat, 'lon': lon,
        'label':                  char.get('label', '–'),
        'embedding':              emb,
        'ux':                     nearest.get('ux', 0.5) if nearest else 0.5,
        'uy':                     nearest.get('uy', 0.5) if nearest else 0.5,
        'semantic_dims':          char.get('char_dims', {}),
        'scale_attention':        [round(x, 4) for x in scale_attn],
        'archetype_similarities': nearest.get('archetype_similarities', {}) if nearest else {},
        'top_pois':               pois,
        'closest_pin_name':       nearest.get('name', '–') if nearest else '–',
        'n_pois':                 char.get('n_pois', len(sal)),
        'avg_saliency':           round(float(sal_n.mean()), 4),
        'confidence':             confidence,
        'confidence_detail':      conf_result,
        'multiscale':             multiscale,
        'char_source':            char.get('source', 'v2'),
    }

