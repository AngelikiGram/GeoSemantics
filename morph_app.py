    ids = body.get('ids', [])
    
    # Fallback to old format if 'ids' isn't explicitly passed
    if not ids and 'id_a' in body and 'id_b' in body:
        ids = [int(body['id_a']), int(body['id_b'])]
        
    locs = []
    for loc_id in ids:
        l = _loc_by_id(int(loc_id))
        if l:
            locs.append(l)
            
    if len(locs) < 2:
        return jsonify({'error': 'Need at least 2 valid locations.'})

    # Return distances between all pairs
    pairwise = []
    for i in range(len(locs)):
        for j in range(i + 1, len(locs)):
            a, b = locs[i], locs[j]
            sim = None
            if a.get('embedding') and b.get('embedding'):
                sim = round(_cosine(a['embedding'], b['embedding']), 4)
            sim_pct = round((sim + 1) / 2 * 100) if sim is not None else 0
            
            da, db = a.get('semantic_dims', {}), b.get('semantic_dims', {})
            all_keys = set(da) | set(db)
            deltas = {k: round(db.get(k, 0) - da.get(k, 0), 4) for k in all_keys}
            
            pairwise.append({
                'loc1_idx': i,
                'loc2_idx': j,
                'loc1_id': a['id'],
                'loc2_id': b['id'],
                'embedding_sim': sim,
                'similarity_pct': sim_pct,
                'semantic_deltas': deltas
            })

    return jsonify({
        'locations': locs,
        'pairwise': pairwise
    })

