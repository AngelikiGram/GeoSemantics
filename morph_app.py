def _free_port(port):
    """Kill any existing processes listening on the given port (Windows-safe)."""
    import subprocess, sys
    try:
        result = subprocess.run(
            ['netstat', '-ano'],
            capture_output=True, text=True, timeout=5
        )
        own_pid = os.getpid()
        for line in result.stdout.splitlines():
            parts = line.split()
            if len(parts) < 5:
                continue
            addr, state, pid_str = parts[1], parts[3], parts[4]
            if f':{port}' in addr and state == 'LISTENING':
                try:
                    pid = int(pid_str)
                    if pid != own_pid:
                        subprocess.run(['taskkill', '/F', '/PID', str(pid)],
                                       capture_output=True, timeout=5)
                        print(f'[morph] Freed port {port} (killed PID {pid})')
                except Exception:
                    pass
    except Exception as e:
        print(f'[morph] Port-cleanup skipped: {e}')

if __name__ == '__main__':
    _free_port(5001)
    print('[morph] Starting GeoSemantics-Morph on http://localhost:5001')
    app.run(port=5001, debug=False, threaded=True)
