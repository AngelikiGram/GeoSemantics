# Helpers
def _cosine(a, b):
    a, b = np.asarray(a, dtype=float), np.asarray(b, dtype=float)
    na, nb = np.linalg.norm(a), np.linalg.norm(b)
    return float(np.dot(a, b) / (na * nb)) if na > 0 and nb > 0 else 0.0

def _haversine(lat1, lon1, lat2, lon2):
    R = 6_371_000
    p, q   = math.radians(lat1), math.radians(lat2)
    dp, dl = math.radians(lat2 - lat1), math.radians(lon2 - lon1)
    a = math.sin(dp / 2) ** 2 + math.cos(p) * math.cos(q) * math.sin(dl / 2) ** 2
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))

def _nearest_geo(lat, lon):
    best_id, best_d = -1, float('inf')
    for loc in _data['locations']:
        d = _haversine(lat, lon, loc['lat'], loc['lon'])
        if d < best_d:
            best_d, best_id = d, loc['id']
    return best_id, best_d

def _nearest_emb(embedding):
    best_id, best_sim = -1, -2.0
    for loc in _data['locations']:
        if not loc.get('embedding'):
            continue
        if len(embedding) != len(loc['embedding']):
            continue
        s = _cosine(embedding, loc['embedding'])
        if s > best_sim:
            best_sim, best_id = s, loc['id']
    return best_id

def _loc_by_id(loc_id):
    return next((l for l in _data['locations'] if l['id'] == loc_id), None)

def _lerp_dims(a, b, t):
    keys = set(a) | set(b)
    raw  = {k: (1 - t) * a.get(k, 0) + t * b.get(k, 0) for k in keys}
    total = sum(raw.values()) + 1e-9
    return {k: round(v / total, 4) for k, v in raw.items()}

def _lerp_list(a, b, t):
    return [round((1 - t) * x + t * y, 4) for x, y in zip(a, b)]

def _lerp_arch(a, b, t):
    keys = set(a) | set(b)
    return {k: round((1 - t) * a.get(k, 0) + t * b.get(k, 0), 4) for k in keys}


