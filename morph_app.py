    top_n     = int(body.get('top_n', 150))
    model_req = body.get('model', None)  # 'v2', 'v3', or None

    try:
        import inference as inf
        import torch

        # Model selection logic
        use_v3 = False
        use_v2 = False
        if model_req == 'v1':
            return jsonify({'edges': [], 'scale_m': 200, 'n_nodes': 0, 'type_names': [], 'model': 'v1'})
        elif model_req == 'v3':
            use_v3 = True
        elif model_req == 'v2':
            use_v2 = True
        else:
            # Auto: prefer v3 if available, else v2
            if getattr(inf, 'v3_available', False):
                use_v3 = True
            elif getattr(inf, 'v2_available', False):
                use_v2 = True

        if use_v3:
            if not getattr(inf, 'v3_available', False):
                return jsonify({'error': 'V3 model not available — train geosemantics_v3.pt first'}), 503
            from geosemantics_v3 import build_multiscale_graphs_v3, SCALES, NODE_TYPE_NAMES
            res    = inf._get_v3_resources()
            graphs = build_multiscale_graphs_v3(lat, lon, external_resources=res)
            if graphs is None:
                return jsonify({'error': 'No graph data at this location'}), 404
            sidx  = max(0, min(scale_idx, len(graphs) - 1))
            g     = graphs[sidx]
            model = inf._v3_model
            with torch.no_grad():
                edges = model.get_attention_edges(g, sidx, top_n=top_n)
            # Normalise weights to [0, 1] for line-thickness mapping in frontend
            if edges:
                w_max = max(e['weight'] for e in edges) or 1.0
                for e in edges:
                    e['weight_norm'] = round(e['weight'] / w_max, 4)
            return jsonify({
                'edges':      edges,
                'scale_m':    SCALES[sidx],
                'n_nodes':    int(g.x.size(0)),
                'type_names': NODE_TYPE_NAMES,
                'model':      'v3',
            })
        elif use_v2:
            if not getattr(inf, 'v2_available', False):
                return jsonify({'error': 'V2 model not available — train geosemantics_v2.pt first'}), 503
            from geosemantics_v2 import build_multiscale_graphs, SCALES
            res    = inf._v2_resources
            graphs = build_multiscale_graphs(lat, lon, external_resources=res)
            if graphs is None:
                return jsonify({'error': 'No graph data at this location'}), 404
            sidx  = max(0, min(scale_idx, len(graphs) - 1))
            g     = graphs[sidx]
            model = inf._v2_model
            with torch.no_grad():
                edges = model.get_attention_edges(g, sidx, top_n=top_n)
            if edges:
                w_max = max(e['weight'] for e in edges) or 1.0
                for e in edges:
                    e['weight_norm'] = round(e['weight'] / w_max, 4)
            # V2 is homogeneous: all nodes are POI type (0)
            type_names = ['POI']
            return jsonify({
                'edges':      edges,
                'scale_m':    SCALES[sidx],
                'n_nodes':    int(g.x.size(0)),
                'type_names': type_names,
                'model':      'v2',
            })
        else:
            return jsonify({'error': 'No supported model available (v2 or v3)'}), 503
    except Exception as exc:
        import traceback; traceback.print_exc()
        return jsonify({'error': str(exc)}), 500

