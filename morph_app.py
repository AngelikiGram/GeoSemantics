# Feature 1 — Reverse Semantic Search
_DIM_KEYS = ['Urban', 'Tourism', 'Heritage', 'Nature', 'Transport', 'Infrastructure', 'Community']

def _char_sim_local(d1, d2):
    """Euclidean character similarity with Nature<->Urban contrastive penalty."""
    v1 = [d1.get(k, 0.0) for k in _DIM_KEYS]
    v2 = [d2.get(k, 0.0) for k in _DIM_KEYS]
    dist = sum((a - b) ** 2 for a, b in zip(v1, v2)) ** 0.5 / 1.414
    sim = max(0.0, 1.0 - dist)
    nat1, urb1 = d1.get('Nature', 0), d1.get('Urban', 0)
    nat2, urb2 = d2.get('Nature', 0), d2.get('Urban', 0)
    contrast = (nat1 - urb1) * (urb2 - nat2)
    if contrast > 0.05:
        sim = max(0.0, sim - min(0.40, contrast * 0.65))
    return round(sim, 4)

@app.route('/api/morph/semantic_search', methods=['POST'])
def semantic_search():
    """Find precomputed locations matching a target semantic profile."""
    body   = request.get_json(force=True)
    target = body.get('dims', {})
    top_k  = int(body.get('top_k', 30))

    total = sum(target.values()) or 1
    target = {k: round(v / total, 4) for k, v in target.items() if v > 0}

    results = []
    for loc in _data['locations']:
        dims = loc.get('semantic_dims', {})
        if not dims:
            continue
        sim = _char_sim_local(target, dims)
        results.append({
            'name':          loc.get('name', '?'),
            'lat':           loc['lat'],
            'lon':           loc['lon'],
            'label':         loc.get('label', '-'),
            'id':            loc['id'],
            'similarity':    sim,
            'semantic_dims': dims,
            'confidence':    loc.get('confidence', 0.5),
        })
    results.sort(key=lambda x: x['similarity'], reverse=True)
    # Always return at least 5 closest matches even if similarity is low
    out = results[:max(top_k, 5)]
    return jsonify({'results': out, 'n_total': len(results), 'target': target})

